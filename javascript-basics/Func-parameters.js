//STRING params
function addTwoStr(valOne, valTwo) {
  console.log(`${valOne} ${valTwo}`);
}
addTwoStr('Glenn', 'Ludszuweit');

function sayHello(name) {
  console.log('Hello ' + name);
}
sayHello('Tony');
sayHello('Gregg');

//NUMBER params
function timeInEurope(london, germany) {
  console.log(london + germany  + ' years ' + 'in Europe');
}
timeInEurope(6, 2);